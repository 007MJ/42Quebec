#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	lens;
	char			*str;
	unsigned int	i;

	i = 0;
	lens = ft_strlen(s);
	str = malloc ((lens + 1) * sizeof(*s));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
